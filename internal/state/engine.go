func managedExecutionUnitBase(name string) string {
	name = strings.ToLower(strings.TrimSpace(name))
	if name == "" {
		return "crateos-managed"
	}
	var b strings.Builder
	for _, r := range name {
		switch {
		case r >= 'a' && r <= 'z':
			b.WriteRune(r)
		case r >= '0' && r <= '9':
			b.WriteRune(r)
		case r == '-' || r == '_':
			b.WriteRune(r)
		default:
			b.WriteRune('-')
		}
	}
	out := strings.Trim(b.String(), "-")
	if out == "" {
		out = "managed"
	}
	return "crateos-" + out
}

