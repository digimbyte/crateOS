func writeManagedActorOwnershipState(index managedActorOwnershipIndex) {
	snapshot := ActorOwnershipState{
		Claims: make([]ActorOwnershipStateItem, 0, len(index.Actors)),
	}
	for _, actor := range index.Actors {
		item := ActorOwnershipStateItem{
			Crate:     actor.Crate,
			Name:      actor.Name,
			Type:      actor.Type,
			ID:        actor.ID,
			User:      actor.User,
			Group:     actor.Group,
			Home:      actor.Home,
			UpdatedAt: actor.UpdatedAt,
			RetiredAt: actor.RetiredAt,
			Status:    "retired",
		}
		if actor.Active {
			item.Status = "claimed"
			snapshot.Active++
		} else {
			snapshot.Retired++
		}
		snapshot.Claims = append(snapshot.Claims, item)
	}
	writeActorOwnershipState(snapshot)
}

func writeManagedExecutionArtifacts(desired config.ServiceEntry, crate CrateState, hasMod bool) {
	if hasMod || crate.ExecutionAdapter != "systemd" || crate.PrimaryUnitPath == "" {
		return
	}
	serviceContent := renderManagedServiceUnit(desired, crate)
	if _, err := writeManagedArtifact(
		filepath.ToSlash(filepath.Join("services", desired.Name, "runtime", "rendered", "systemd", crate.PrimaryUnit)),
		crate.PrimaryUnitPath,
		serviceContent,
		"service",
		fmt.Sprintf("rendered managed execution unit %s", crate.PrimaryUnit),
	); err != nil {
		log.Printf("warning: render managed execution unit %s: %v", desired.Name, err)
	}
	if crate.ExecutionMode == "job" && crate.CompanionUnitPath != "" {
		timerContent := renderManagedTimerUnit(desired, crate)
		if _, err := writeManagedArtifact(
			filepath.ToSlash(filepath.Join("services", desired.Name, "runtime", "rendered", "systemd", crate.CompanionUnit)),
			crate.CompanionUnitPath,
			timerContent,
			"service",
			fmt.Sprintf("rendered managed timer unit %s", crate.CompanionUnit),
		); err != nil {
			log.Printf("warning: render managed timer unit %s: %v", desired.Name, err)
		}
	}
}

func renderManagedServiceUnit(desired config.ServiceEntry, crate CrateState) string {
	lines := []string{
		"# Generated by CrateOS.",
		"# Generated native service artifact managed by CrateOS.",
		"[Unit]",
		fmt.Sprintf("Description=CrateOS managed workload for %s", desired.Name),
		"After=network.target",
		"",
		"[Service]",
	}
	if crate.ExecutionMode == "job" {
		lines = append(lines, "Type=oneshot")
	} else {
		lines = append(lines, "Type=simple")
	}
	if crate.ActorUser != "" {
		// TODO: Provision and verify the managed actor account before native activation so User=/Group= never point at a missing identity.
		lines = append(lines, "User="+crate.ActorUser)
	}
	if crate.ActorGroup != "" {
		lines = append(lines, "Group="+crate.ActorGroup)
	}
	if crate.WorkingDir != "" {
		lines = append(lines, "WorkingDirectory="+crate.WorkingDir)
	}
	if crate.ActorRuntimeDir != "" {
		// TODO: Enforce actor runtime directory ownership during reconcile instead of relying on RuntimeDirectory defaults alone.
		lines = append(lines, "RuntimeDirectory="+filepath.Base(crate.ActorRuntimeDir))
		lines = append(lines, "RuntimeDirectoryPreserve=yes")
	}
	if crate.ActorStateDir != "" {
		// TODO: Create and chown actor state paths during apply so hosted workloads do not inherit root-owned state unexpectedly.
		lines = append(lines, "StateDirectory=crateos/"+desired.Name+"/"+filepath.Base(crate.ActorStateDir))
	}
	if crate.EnvironmentFile != "" {
		lines = append(lines, "EnvironmentFile=-"+crate.EnvironmentFile)
	}
	if crate.StartCommand != "" {
		lines = append(lines, "ExecStart=/bin/sh -lc "+quoteSystemdShell(crate.StartCommand))
	} else {
		lines = append(lines, "# ExecStart pending module binding or execution.start_cmd")
	}
	if crate.Timeout != "" && crate.Timeout != "0" {
		lines = append(lines, "RuntimeMaxSec="+crate.Timeout)
	}
	if crate.StopTimeout != "" {
		lines = append(lines, "TimeoutStopSec="+crate.StopTimeout)
	}
	if crate.KillSignal != "" {
		lines = append(lines, "KillSignal="+crate.KillSignal)
	}
	lines = append(lines,
		"# TODO: Add stronger systemd hardening once actor provisioning and ownership enforcement land end-to-end.",
		fmt.Sprintf("# Managed actor pending lifecycle provisioning: %s", valueOrDefault(crate.ActorName, "unassigned")),
		fmt.Sprintf("# Managed actor id: %s", valueOrDefault(crate.ActorID, "pending")),
		fmt.Sprintf("# Managed actor user/group: %s:%s", valueOrDefault(crate.ActorUser, "pending"), valueOrDefault(crate.ActorGroup, "pending")),
		fmt.Sprintf("# Managed actor home: %s", valueOrDefault(crate.ActorHome, "pending")),
		fmt.Sprintf("# Deploy source: %s", valueOrDefault(crate.DeploySource, "unspecified")),
		fmt.Sprintf("# Entry: %s", valueOrDefault(crate.Entry, "unspecified")),
		"",
		"[Install]",
		"WantedBy=multi-user.target",
		"",
	)
	return strings.Join(lines, "\n")
}

func renderManagedTimerUnit(desired config.ServiceEntry, crate CrateState) string {
	onCalendar := translateExecutionSchedule(crate.Schedule)
	lines := []string{
		"# Generated by CrateOS.",
		"# Generated native timer artifact managed by CrateOS.",
		"[Unit]",
		fmt.Sprintf("Description=CrateOS managed schedule for %s", desired.Name),
		"",
		"[Timer]",
		fmt.Sprintf("Unit=%s", crate.PrimaryUnit),
		"Persistent=true",
	}
	if onCalendar != "" {
		lines = append(lines, "OnCalendar="+onCalendar)
	} else {
		// TODO: Replace the narrow cron-like translation with a first-class CrateOS schedule model before exposing richer recurrence rules.
		lines = append(lines, "# OnCalendar translation failed; timer activation remains blocked.")
	}
	lines = append(lines,
		fmt.Sprintf("# Source schedule: %s", valueOrDefault(crate.Schedule, "unspecified")),
		"",
		"[Install]",
		"WantedBy=timers.target",
		"",
	)
	return strings.Join(lines, "\n")
}

func quoteSystemdShell(command string) string {
	command = strings.ReplaceAll(command, "\\", "\\\\")
	command = strings.ReplaceAll(command, "\"", "\\\"")
	return "\"" + command + "\""
}

func valueOrDefault(value, fallback string) string {
	if strings.TrimSpace(value) == "" {
		return fallback
	}
	return value
}

func managedActorIDSeed(crateName, actorName string, ordinal int) string {
	base := strings.TrimSpace(actorName)
	if base == "" {
		base = strings.TrimSpace(crateName)
	}
	seed := fmt.Sprintf("%s|%s|%d", strings.TrimSpace(crateName), base, ordinal)
	sum := sha256.Sum256([]byte(seed))
	hexID := hex.EncodeToString(sum[:16])
	return fmt.Sprintf("%s-%s-%s-%s-%s", hexID[0:8], hexID[8:12], hexID[12:16], hexID[16:20], hexID[20:32])
}

func managedActorAccountName(actorID string, ordinal int) string {
	compact := strings.ReplaceAll(strings.TrimSpace(actorID), "-", "")
	if compact == "" {
		return ""
	}
	suffix := ""
	if ordinal > 0 {
		suffix = fmt.Sprintf("-%d", ordinal)
	}
	maxCompact := 32 - len("ca-") - len(suffix)
	if maxCompact > len(compact) {
		maxCompact = len(compact)
	}
	if maxCompact < 8 {
		maxCompact = 8
	}
	return "ca-" + compact[:maxCompact] + suffix
}

