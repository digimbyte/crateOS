func aggregateCrateStatus(units []ServiceState) string {
	if len(units) == 0 {
		return "unknown"
	}
	activeCount := 0
	failedCount := 0
	unknownCount := 0
	for _, unit := range units {
		switch unit.Status {
		case "failed":
			failedCount++
		case "active", "running":
			activeCount++
		case "", "unknown":
			unknownCount++
		}
	}
	switch {
	case failedCount > 0:
		return "failed"
	case activeCount == len(units):
		return "active"
	case activeCount > 0:
		return "partial"
	case unknownCount == len(units):
		return "unknown"
	default:
		return "inactive"
	}
}

func aggregateCrateHealth(units []ServiceState) string {
	if len(units) == 0 {
		return "unknown"
	}
	allOK := true
	for _, unit := range units {
		if unit.Health == "degraded" {
			return "degraded"
		}
		if unit.Health != "ok" {
			allOK = false
		}
	}
	if allOK {
		return "ok"
	}
	return "unknown"
}

func summarizeCrateUnits(units []ServiceState) string {
	if len(units) == 0 {
		return ""
	}
	activeCount := 0
	enabledCount := 0
	failed := make([]string, 0)
	inactive := make([]string, 0)
	for _, unit := range units {
		if unit.Active {
			activeCount++
		}
		if unit.Enabled {
			enabledCount++
		}
		if unit.Status == "failed" {
			failed = append(failed, unit.Name)
			continue
		}
		if !unit.Active {
			inactive = append(inactive, unit.Name)
		}
	}
	switch {
	case len(failed) > 0:
		return "failed units: " + strings.Join(failed, ", ")
	case activeCount > 0 && activeCount < len(units):
		return fmt.Sprintf("partially active (%d/%d units running)", activeCount, len(units))
	case enabledCount > 0 && enabledCount < len(units):
		return fmt.Sprintf("partially enabled (%d/%d units enabled)", enabledCount, len(units))
	case len(inactive) == len(units):
		return "all units inactive"
	default:
		return ""
	}
}

func crateUnits(name string, mod modules.Module, hasMod bool) []string {
	if units := modules.ResolveUnits(name, mod, hasMod); len(units) > 0 {
		return units
	}
	return nil
}

func crateUnitStates(actual []ServiceState, units []string) []ServiceState {
	states := make([]ServiceState, 0, len(units))
	for _, unit := range units {
		found := false
		for _, svc := range actual {
			if svc.Name == unit {
				states = append(states, svc)
				found = true
				break
			}
		}
		if !found {
			states = append(states, ServiceState{Name: unit, Status: "unknown", Health: "unknown"})
		}
	}
	return states
}

func containsServiceState(states []ServiceState, name string) bool {
	for _, state := range states {
		if state.Name == name {
			return true
		}
	}
	return false
}

func appendUnique(slice []string, items ...string) []string {
	seen := make(map[string]bool, len(slice))
	for _, s := range slice {
		seen[s] = true
	}
	for _, item := range items {
		if !seen[item] {
			slice = append(slice, item)
			seen[item] = true
		}
	}
	return slice
}

func applyManagedExecutionNativeState(desired config.ServiceEntry, crate CrateState, hasMod bool) {
	if runtime.GOOS != "linux" || hasMod || crate.ExecutionAdapter != "systemd" {
		return
	}
	_, _, _ = syncManagedExecutionNativeUnits(desired, crate)
}

func syncManagedExecutionNativeUnits(desired config.ServiceEntry, crate CrateState) ([]Action, bool, []string) {
	actions := []Action{}
	reloaded := false
	issues := []string{}
	provisionActions, provisionIssues := prepareHostedManagedActor(desired, crate)
	actions = append(actions, provisionActions...)
	if len(provisionIssues) > 0 {
		issues = append(issues, provisionIssues...)
		return actions, reloaded, issues
	}
	// TODO: Materialize deploy uploads before copying units into /etc/systemd/system.
	primaryNativePath := managedNativeUnitPath(crate.PrimaryUnit)
	if crate.PrimaryUnitPath != "" && primaryNativePath != "" {
		if action, err := syncManagedArtifact(filepath.ToSlash(filepath.Join("native/services", desired.Name, crate.PrimaryUnit)), crate.PrimaryUnitPath, primaryNativePath, "service", fmt.Sprintf("installed hosted workload unit %s", crate.PrimaryUnit)); err != nil {
			issues = append(issues, err.Error())
		} else if action != nil {
			actions = append(actions, *action)
			reloaded = true
		}
	}
	companionNativePath := managedNativeUnitPath(crate.CompanionUnit)
	if crate.CompanionUnitPath != "" && companionNativePath != "" {
		if action, err := syncManagedArtifact(filepath.ToSlash(filepath.Join("native/services", desired.Name, crate.CompanionUnit)), crate.CompanionUnitPath, companionNativePath, "service", fmt.Sprintf("installed hosted workload timer %s", crate.CompanionUnit)); err != nil {
			issues = append(issues, err.Error())
		} else if action != nil {
			actions = append(actions, *action)
			reloaded = true
		}
	}
	if !desired.Enabled {
		if action, err := removeManagedArtifact(filepath.ToSlash(filepath.Join("native/services", desired.Name, crate.PrimaryUnit)), primaryNativePath, "service", fmt.Sprintf("removed hosted workload unit %s", crate.PrimaryUnit)); err != nil {
			issues = append(issues, err.Error())
		} else if action != nil {
			actions = append(actions, *action)
			reloaded = true
		}
		if companionNativePath != "" {
			if action, err := removeManagedArtifact(filepath.ToSlash(filepath.Join("native/services", desired.Name, crate.CompanionUnit)), companionNativePath, "service", fmt.Sprintf("removed hosted workload timer %s", crate.CompanionUnit)); err != nil {
				issues = append(issues, err.Error())
			} else if action != nil {
				actions = append(actions, *action)
				reloaded = true
			}
		}
	}
	return actions, reloaded, issues
}

func managedNativeUnitPath(unit string) string {
	unit = strings.TrimSpace(unit)
	if unit == "" {
		return ""
	}
	return filepath.Join("/etc/systemd/system", unit)
}

func activateHostedManagedWorkloads(cfg *config.Config, actual *ActualState, mods map[string]modules.Module) []Action {
	if runtime.GOOS != "linux" {
		return nil
	}
	var actions []Action
	var reloadNeeded bool
	actualByName := make(map[string]ServiceState, len(actual.Services))
	for _, svc := range actual.Services {
		actualByName[svc.Name] = svc
	}
	for _, desired := range cfg.Services.Services {
		mod, hasMod := mods[desired.Name]
		if hasMod {
			continue
		}
		crate := buildCrateState(desired, actualByName, mod, hasMod)
		// TODO: Stop rebuilding transient crate state twice once apply can persist provisioning results back into runtime posture.
		syncActions, reloaded, issues := syncManagedExecutionNativeUnits(desired, crate)
		actions = append(actions, syncActions...)
		if len(issues) > 0 {
			continue
		}
		if reloaded {
			reloadNeeded = true
		}
	}
	if reloadNeeded {
		if err := systemctl("daemon-reload"); err == nil {
			actions = append(actions, Action{Description: "reloaded systemd after hosted workload unit changes", Component: "service", Target: "systemd"})
		}
	}
	for _, desired := range cfg.Services.Services {
		mod, hasMod := mods[desired.Name]
		if hasMod || !desired.Enabled {
			continue
		}
		crate := buildCrateState(desired, actualByName, mod, hasMod)
		actions = append(actions, activateHostedManagedUnits(desired, crate, actualByName)...)
	}
	return actions
}

func activateHostedManagedUnits(desired config.ServiceEntry, crate CrateState, actualByName map[string]ServiceState) []Action {
	var actions []Action
	switch crate.ExecutionMode {
	case "job":
		timerName := crate.CompanionUnit
		if timerName == "" || translateExecutionSchedule(crate.Schedule) == "" {
			return actions
		}
		if _, issues := prepareHostedManagedActor(desired, crate); len(issues) > 0 {
			return actions
		}
		// TODO: Enforce CrateOS concurrency and timeout policy for jobs explicitly; timer activation alone does not guarantee overlap behavior.
		timerState := actualByName[timerName]
		if !timerState.Enabled {
			if err := systemctl("enable", timerName); err == nil {
				actions = append(actions, Action{Description: fmt.Sprintf("enabled hosted workload timer %s for %s", timerName, desired.Name), Component: "service", Target: timerName})
			}
		}
		if !timerState.Active {
			if err := systemctl("start", timerName); err == nil {
				actions = append(actions, Action{Description: fmt.Sprintf("started hosted workload timer %s for %s", timerName, desired.Name), Component: "service", Target: timerName})
			}
		}
	default:
		serviceName := crate.PrimaryUnit
		if serviceName == "" {
			return actions
		}
		if _, issues := prepareHostedManagedActor(desired, crate); len(issues) > 0 {
			return actions
		}
		serviceState := actualByName[serviceName]
		if !serviceState.Enabled {
			if err := systemctl("enable", serviceName); err == nil {
				actions = append(actions, Action{Description: fmt.Sprintf("enabled hosted workload service %s for %s", serviceName, desired.Name), Component: "service", Target: serviceName})
			}
		}
		if desired.Autostart && !serviceState.Active {
			if err := systemctl("start", serviceName); err == nil {
				actions = append(actions, Action{Description: fmt.Sprintf("started hosted workload service %s for %s", serviceName, desired.Name), Component: "service", Target: serviceName})
			}
		}
	}
	return actions
}

func translateExecutionSchedule(value string) string {
	parts := strings.Fields(strings.TrimSpace(value))
	if len(parts) == 0 {
		return ""
	}
	// TODO: Replace this adapter with structured schedule parsing and validation so CrateOS owns recurrence semantics instead of inheriting cron quirks.
	if len(parts) == 5 {
		parts = append([]string{"0"}, parts...)
	}
	if len(parts) != 6 {
		return ""
	}
	for _, part := range parts {
		if !isSupportedScheduleToken(part) {
			return ""
		}
	}
	weekday := normalizeWeekdayToken(parts[5])
	day := normalizeDayToken(parts[3])
	month := normalizeMonthToken(parts[4])
	if weekday == "" || day == "" || month == "" {
		return ""
	}
	return fmt.Sprintf("%s-%s-%s %s:%s:%s", weekday, month, day, parts[2], parts[1], parts[0])
}

func isSupportedScheduleToken(value string) bool {
	value = strings.TrimSpace(value)
	if value == "" {
		return false
	}
	for _, r := range value {
		switch {
		case r >= '0' && r <= '9':
		case r >= 'A' && r <= 'Z':
		case r >= 'a' && r <= 'z':
		case r == '*', r == ',', r == '/', r == '-':
		default:
			return false
		}
	}
	return true
}

func normalizeWeekdayToken(value string) string {
	value = strings.TrimSpace(strings.ToUpper(value))
	if value == "" {
		return ""
	}
	if value == "*" {
		return "*"
	}
	return strings.ReplaceAll(value, "7", "0")
}

func normalizeDayToken(value string) string {
	value = strings.TrimSpace(value)
	if value == "" {
		return ""
	}
	if value == "?" {
		return "*"
	}
	return value
}

func normalizeMonthToken(value string) string {
	value = strings.TrimSpace(strings.ToUpper(value))
	if value == "" {
		return ""
	}
	if value == "?" {
		return "*"
	}
	return value
}
